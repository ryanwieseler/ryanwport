      <h4 id="info" class="clear">Information Architecture</h4>
      <a class="design-process-img" id="watertown-info-arch" href="watertown-info-arch.php"></a>
      <div class="explanation">
      <p>Watertown was a coffee shop by day, bar by night. Using my own experiences as an example, the following is a list of the information customers are seeking when visiting any coffee shop/bar website.</p>
      <ol>
      	  <li>Location</li>
          <li>Happy hour specials</li>
          <li>Menu</li>
          <li>Atmosphere</li>
          <li>Calendar of events</li>
          <li>Hours of operation</li>
        </ol>
        <p>Based on this list, the information was easily segmented into conventional categories.<a class="btt" href="#top"><img class="arrow" src="images/up-arrow.png" width="14" height="14" alt="Up Arrow" />Back to Top</a></p>
