      <h4 id="wire" class="clear">Wireframes</h4>
      <a class="design-process-img" id="watertown-wireframe" href="watertown-wireframes.pdf" target="_blank"></a>
      <div class="explanation">
        <p><strong>Homepage Presentation of Important Information</strong></p>
        <p>All the information that customers are seeking (based on the list above) is prominently displayed on the homepage: Current and daily happy hour specials, address, hours of operation, links to menus and more are clearly displayed to the customer.</p>
        <p><strong>Conventional Navigation and URL List Layout</strong></p>
        <p>The main navigation is based on what information the customer is seeking assuming that they are viewing the site from left to right. The sub-navigation is consistent within each sub-page. These two things help the customer focus on the content and not navigation.</p>
        <p><strong>Visual Hierarchy of Information</strong></p>
        <p>There is a clear visual hierarchy of information that reinforces the strategy and thinking behind why customers are visiting the website. It starts with the main advertisement of the current happy hour specials and ends with the header and footer information, which pertains to location, hours of operation, and contact information.<a class="btt" href="#top"><img class="arrow" src="images/up-arrow.png" width="14" height="14" alt="Up Arrow" />Back to Top</a></p>
